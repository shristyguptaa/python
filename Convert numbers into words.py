# WAP to convert all the numbers between 1 to 10 into words
a = int(input("Enter a number"))
if a == 1:
    print("ONE")
elif a == 2:
    print("TWO")
elif a == 3:
    print("THREE")
elif a == 4:
    print("FOUR")
elif a == 5:
    print("FIVE")
elif a == 6:
    print("SIX")
elif a == 7:
    print("SEVEN")
elif a == 8:
    print("EIGHT")
elif a == 9:
    print("NINE")
elif a == 10:
    print("TEN")
else:
    print("Enter a valid number")
